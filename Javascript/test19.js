const month = "March" 

switch (month) {
    case "jan":
        console.log("Janurary");
        break;
    case "feb":
        console.log("Febrary");
        break;
    case "March":
        console.log("March");
        break;
    case 4:
        console.log("April");
        break;
    case 5:
        console.log("may");
        break;
    
    

    default:
        console.log("default case match");
        break;
}